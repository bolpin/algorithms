
// --- Directions
//   Implement Conway's Game of Life for an M x N input array
//   of 1s and 0s
//
//   Rules:
// Any live cell with fewer than two live neighbours dies, as if by underpopulation.
// Any live cell with two or three live neighbours lives on to the next generation.
// Any live cell with more than three live neighbours dies, as if by overpopulation.
// Any dead cell with exactly three live neighbours becomes a live cell, as if by reproduction.

// Sample Input

function countLivingNeighbors(board, x, y) {
  let minX = 0;
  let minY = 0;
  let maxX = board.length-1;
  let maxY = board[0].length-1;
  let count = 0;
  for (let i = x-1; i <= x+1; i++) {
    for (let j = y-1; j <= y+1; j++) {
      // console.log(`${i} : ${j}`)
      if (i >= minX &&
        i <= maxX &&
        j >= minY &&
        j <= maxY &&
        !(i === x && j === y) &&
        board[i][j] === 1
      ) {
        count += 1;
      }
    }
  }
  return count;
}

// returns a 1 or 0
function evolveState(board, x, y) {
  const n = countLivingNeighbors(board, x, y);
  newState = 0;
  if (board[x][y] === 1 && (n === 2 || n === 3)) {
    newState = 1;
  }
  if (board[x][y] === 0 && n === 3) {
    newState = 1;
  }
  return newState;
}

