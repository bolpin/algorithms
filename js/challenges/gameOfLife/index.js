function step(board) {
  let result = []
  for(let i = 0; i < board.length; i++) {
    result.push(new Array(board[0].length))
  }

  for(let i = 0; i < board.length; i++) {
    for(let j = 0; j < board[0].length; j++) {
      let n = evolveState(board, i, j)
      result[i][j] = n;
      // console.log(`${i}: ${j}, ${n}`)
    }
  }
  return result;
}
